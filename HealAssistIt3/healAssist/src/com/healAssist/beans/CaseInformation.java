package com.healAssist.beans;
public class CaseInformation {
	private String caseId;
	private String caseRemarks;
	private String caseDateTime;
	
	
	
	public String getCaseId() {
		return caseId;
	}
	public void setCaseId(String caseId) {
		this.caseId = caseId;
	}
	public String getCaseRemarks() {
		return caseRemarks;
	}
	public void setCaseRemarks(String caseRemarks) {
		this.caseRemarks = caseRemarks;
	}
	public String getCaseDateTime() {
		return caseDateTime;
	}
	public void setCaseDateTime(String caseDateTime) {
		this.caseDateTime = caseDateTime;
	}
	
	

}
